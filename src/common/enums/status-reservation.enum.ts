/**
 * Enum for status reservation
 * PENDING - The reservation is pending approval.
 * APPROVED - The reservation has been approved.
 * REJECTED - The reservation has been rejected.
 * CANCELLED - The reservation has been cancelled.
 */
export enum StatusReservation {
  PENDING = 'PENDING',
  APPROVED = 'APPROVED',
  REJECTED = 'REJECTED',
  CANCELLED = 'CANCELLED'
}
