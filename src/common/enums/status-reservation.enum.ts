/**
 * Enumeration for reservation status options.
 * @enum {string}
 */
export enum StatusReservation {
  /**
   * Reservation approved.
   * @example
   * StatusReservation.APPROVED
   */
  APPROVED = 'APPROVED',

  /**
   * Reservation rejected.
   * @example
   * StatusReservation.REJECTED
   */
  REJECTED = 'REJECTED'
}
